// Helper function to get wire area
export const getWireArea = (wireType, size) => {
  return wireData[wireType]?.[size] || 0;
};

// Helper function to get available sizes for a wire type
export const getAvailableWireSizes = (wireType) => {
  if (!wireData[wireType]) return [];
  return wireSizeOrder.filter(size => wireData[wireType][size] !== undefined);
};